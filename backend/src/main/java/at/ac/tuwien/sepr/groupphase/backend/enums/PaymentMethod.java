package at.ac.tuwien.sepr.groupphase.backend.enums;

/**
 * Enum that describes the different payment methods available in the hotel.
 * <ul>
 *     <li>{@code PayInAdvance}: The payment is made before the stay, typically at the time of booking.</li>
 *     <li>{@code PayCash}: The payment is made in cash during or after the stay.</li>
 * </ul>
 */
public enum PaymentMethod {
    PayInAdvance,
    PayCash
}
